void Dbg_Log_Print(Dbg_Feat_Id_T const fid, uint8_t const instance, char const * file,
      Dbg_Verbose_Lvl_T const info, int const line, char const * fmt, ...)
{
   va_list args;
   va_start(args, fmt);
   printf("%d.%d: %s:%d:%s - ", fid, instance, file, line, Dbg_Lvl_Name[info]);
   vprintf(fmt, args);
   printf("\n");
   va_end(args);
}
